def stringToHex(s):
    val = ""
    for k in s:
        if val == "":
            val = str(hex(ord(k)))
        else:
            val += str(hex(ord(k)))
    return val.replace("0x", "")


def get_cookie(url):
    screendate = "1366,768"  # 屏幕宽度和高度．
    curlocation = url  # 当前请求的url
    cookie = "srcurl=" + stringToHex(curlocation) + ";path=/;"
    cookie = {"srcurl": cookie}
    url = url + "&security_verify_data=" + stringToHex(screendate)
    return url, cookie


